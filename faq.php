        <h3>Perguntas Frequentes;</h3>
        <hr>
        <br>
        <p>
1.  É NECESSÁRIO TER UM ANTIVÍRUS INSTALADO NO COMPUTADOR?
 Sim. Um Software Antivírus é necessário para proteger o seu computador contra ameaças virtuais, como vírus, trojans, etc. Um computador sem antivírus é um alvo mais fácil para essas ameaças da internet. Mas só ter um antivírus não resolve, você tem que saber navegar com segurança.
<br>
<br>
<br>
2.  É VERDADE QUE LINUX NÃO PEGA VÍRUS?
 O Linux é um sistema operacional diferente do Windows. O Linux não pega vírus porque os vírus são criados para Windows, geralmente em arquivos “.exe”, “.com” e “.bat”, que são arquivos que não são executados no Linux.
Ou seja, o Linux não só pega vírus, quanto não executa os executáveis do Windows. (Mas existe uma maneira de emulá-los)
<br>
<br>
3.  POSSO REMOVER DISPÓSITIVO REMOVÍVEL (PENDRIVE) SEM AVISAR A MÁQUINA?
 Não! deve ser selecionada a opção ‘Retirar hardware com segurança’ antes de retirá-lo. Caso contrário, corre-se o risco de queimar.
<br>
<br>
<br>
4.  AO DESLIGAR O COMPUTADOR CONVÉM TAMBEM DESLIGAR O MONITOR?
 Tanto faz. Ao desligar a máquina, o monitor fica num estado em que consome muito pouca energia (pouca coisa mais que 1W) e não sofre desgaste algum. A decisão termina sendo em função da economia, ainda que o consumo seja realmente mínimo.
<br>
<br>
<br>
5.  QUANDO HÁ CHUVA FORTE, É ABSOLUTAMENTE NECESSARIO TIRAR O PLUGUE DO COMPUTADOR DA TOMADA?
 Sim! Deveria ser adotado como uma obrigação no caso de uma chuva muito forte, com muitos raios e trovões. Da mesma forma, é aconselhável retirar os cabos do telefone e da alimentação do modem para que não queimem com a descarga de raios.
<br>
<br>
<br>
6.  QUANDO O COMPUTADOR PASSOU A NOITE TODA LIGADO, É MELHOR DESLIGA-LO E VOLTAR A REINICIAR?
 Pode seguir ligado sem problema algum. Ainda que pareça o contrário e dê vontade de desligá-lo um momento para que descanse, seguindo a lógica humana, o HD dura muito mais se permanecer ligado e não sendo o tempo todo ligado e desligado. Por uma questão de economia de energia, não convém deixar ligado por vários dias, mas se não levarmos em conta o fator do aquecimento global seria muito melhor para o PC nunca desligá-lo. Eles foram criados para isso.
<br>
<br>
<br>
7.  GASTA MAIS ENERGIA AO SER LIGADO O COMPUTADOR DO QUE EM VÁRIAS HORAS DE USO?
 Ao ligar não consome tanto como para superar as horas de funcionamento. Ao desligar poupa-se energia e se permanecer ligado gasta, como qualquer outro eletrônico.
<br>
<br>
<br>
8.  QUAL É O MELHOR MICRO PARA JOGOS?
 Complicado especificar a melhor configuração, a cada dia estão sendo lançados mais jogos e cada vez exigindo mais ainda do hardware, mas podemos fazer uma avaliação do seu perfil e assim decidir a melhor configuração para ser adquirida.
<br>
<br>
<br>
9.  POR QUE TEHNHO QUE FORMATAR MEU MICRO?
 Esta é uma pergunta muito comum entre as pessoas que não se conformam que o seu windows lento pede uma formatação.
A necessidade de formatação ocorre por vários motivos, entre eles:
 
•	Lentidão
•	Erro de Windows
•	Vírus 
 
Entre outras, porem de qualquer maneira nos iremos avaliar seu equipamento e assim decidir o melhor método para solucionar seu problema com total segurança e agilidade no nosso serviço.
<br>
<br>
<br>
10. O COMPUTADOR NUNCA PODE FICAR AO SOL?
Se ele esquentar mais do que o habitual, sua vida útil tende a decrescer. Por isso nunca é boa ideia instalar o PC próximo a janelas onde bate o sol.
